/**
 * 关系运算符
 * */

package main

import "fmt"

func main() {
	var a int = 21
	var b int = 10

	fmt.Println("a = ", a, ", b = ", b)

	fmt.Println("a == b : ", a == b)
	fmt.Println("a != b :", a != b)
	fmt.Println("a > b :", a > b)
	fmt.Println("a < b :", a < b)
	fmt.Println("a >= b :", a >= b)
	fmt.Println("a <= b :", a <= b)

}